from typing import List

"""
给定一个由 0 和 1 组成的矩阵，找出每个元素到最近的 0 的距离。

两个相邻元素间的距离为 1 。

示例 1:
输入:

0 0 0
0 1 0
0 0 0
输出:

0 0 0
0 1 0
0 0 0
示例 2:
输入:

0 0 0
0 1 0
1 1 1
输出:

0 0 0
0 1 0
1 2 1
注意:

给定矩阵的元素个数不超过 10000。
给定矩阵中至少有一个元素是 0。
矩阵中的元素只在四个方向上相邻: 上、下、左、右。

来源：力扣（LeetCode）
链接：https://leetcode-cn.com/problems/01-matrix
著作权归领扣网络所有。商业转载请联系官方授权，非商业转载请注明出处。
"""


class Solution:
    def updateMatrix(self, matrix: List[List[int]]) -> List[List[int]]:
        M, N = len(matrix), len(matrix[0])
        Q = []
        level = {}
        for i in range(0, M):
            for j in range(0, N):
                if matrix[i][j] == 0:
                    Q.append((i, j))
                    level[(i, j)] = 0
        ds = [(1, 0), (0, 1), (-1, 0), (0, -1)]
        while Q:
            nQ = []
            for q in Q:
                for d in ds:
                    dni, dnj = q[0] - d[0], q[1] - d[1]
                    if (dni, dnj) not in level and 0 <= dni < M and 0 <= dnj < N:
                        matrix[dni][dnj] = level[(q[0], q[1])] + 1
                        nQ.append((dni, dnj))
                        level[(dni, dnj)] = level[(q[0], q[1])] + 1
            Q = nQ
        return matrix

    def updateMatrix2(self, matrix: List[List[int]]) -> List[List[int]]:
        def extractMinQ(v):
            v.sort(key=lambda x: x[2], reverse=True)
            return v.pop()

        ds = [(1, 0), (0, 1), (-1, 0), (0, -1)]
        Q = []
        S = set()
        M, N = len(matrix), len(matrix[0])
        for i in range(0, M):
            for j in range(0, N):
                if matrix[i][j] == 0:
                    Q.append((i, j, 0))
                    S.add((i, j))
                else:
                    matrix[i][j] = float('inf')
        while Q:
            q = extractMinQ(Q)
            for d in ds:
                dni, dnj = q[0] - d[0], q[1] - d[1]
                if 0 <= dni < M and 0 <= dnj < N:
                    if (dni, dnj) not in S:
                        n, p = matrix[q[0] - d[0]][q[1] - d[1]], matrix[q[0]][q[1]]
                        if n > p + 1:
                            matrix[q[0] - d[0]][q[1] - d[1]] = p + 1
                        Q.append((dni, dnj, n))
            S.add((q[0], q[1]))
        return matrix

    def updateMatrix1(self, matrix: List[List[int]]) -> List[List[int]]:
        ds = [(1, 0), (0, 1), (-1, 0), (0, -1)]
        M, N = len(matrix), len(matrix[0])
        p = 0
        for _ in range(0, max(M, N)):
            g = 0
            for i in range(0, M):
                for j in range(0, N):
                    mm = float('inf')
                    for d in ds:
                        p += 1
                        if 0 <= i - d[0] < M and 0 <= j - d[1] < N:
                            mm = min(matrix[i - d[0]][j - d[1]], mm)
                    if mm and matrix[i][j] and matrix[i][j] - mm != 1:
                        matrix[i][j] = mm + 1
                        g += 1
            if not g:
                break
        return matrix


