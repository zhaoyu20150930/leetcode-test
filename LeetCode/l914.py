class Solution:
    def fgdc(self, f, d):
        f, d = max(f, d), min(f, d)
        while f % d:
            f, d = d, f % d
        return d

    def hasGroupsSizeX(self, deck: List[int]) -> bool:
        dn = {i: deck.count(i) for i in set(deck)}
        f = 0
        for d in dn.values():
            f = self.fgdc(f if f else d, d)
            if f == 1:
                return False
        return True if f else False


